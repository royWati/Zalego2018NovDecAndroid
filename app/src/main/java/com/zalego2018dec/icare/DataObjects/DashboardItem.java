public class DashboardItem {

    // this are the contents for our dashboard items
    private int icon;
    private String text;

    //creating getters and setters for our variables

    public int getIcon() {
        return icon;
    }

    public void setIcon(int icon) {
        this.icon = icon;
    }

    public String getText() {
        return text;
    }

    public void setText(String text) {
        this.text = text;
    }
}
